const div = require('../div');
const test = require('tape');

test('div', function (t) {
  var a = [1, 2, 2, 3]
  var b = [3, -5, 1, -2]
  t.deepEqual(div.ss([], a, b), [
    -0.20588235294117646,
    0.3235294117647059
  ])
  t.deepEqual(div.sd([], a, b), [
    -0.20588235294117646,
    0.3235294117647059,
    0.06920415224913495,
    -0.12975778546712805
  ])
  t.deepEqual(div.ds([], a, b), [
    -0.20588235294117646,
    0.3235294117647059,
    -0.2647058823529412,
    0.5588235294117647
  ])
  t.deepEqual(div.dd([], a, b), [
    -0.20588235294117646,
    0.3235294117647059,
    -0.1955017301038062,
    0.42906574394463676
  ])
  
  t.end();
});
